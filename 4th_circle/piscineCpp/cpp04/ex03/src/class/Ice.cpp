#include "Ice.hpp"

/******************************************************************************/
/*   CONSTRUCTORS / DESTRUCTORS                                               */
/******************************************************************************/
Ice::Ice( void ) : _type( "ice" ) {}

Ice::Ice( Ice const & src ) : _type( src._type ) {}

Ice::~Ice( void ) {}

/******************************************************************************/
/*   ASSIGNATION OPERATOR                                                     */
/******************************************************************************/
Ice&	Ice::operator=( Ice const & rhs ) {

	if ( this != &rhs ) {

		_type = rhs._type;
	}

	return *this;
}

