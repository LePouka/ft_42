#include "Cat.hpp"

/******************************************************************************/
/*   CONSTRUCTORS / DESTRUCTORS                                               */
/******************************************************************************/
Cat::Cat( void ) : _type( "Cat" ) {}

Cat::Cat( Cat const & src ) : _type( src._type ) {}

Cat::~Cat( void ) {}

/******************************************************************************/
/*   ASSIGNATION OPERATOR                                                     */
/******************************************************************************/
Cat&	Cat::operator=( Cat const & rhs ) {

	_type = rhs._type;

	return *this;
}

/******************************************************************************/
/*   GETTERS                                                                  */
/******************************************************************************/
std::string	Cat::getType( void ) const {

	return _type;
}

