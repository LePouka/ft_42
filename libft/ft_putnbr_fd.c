#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == INT_MIN)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', fd);
			n *= -1;
		}
		if (n >= 9)
			ft_putchar_fd((n / 10), fd);
		ft_putnbr_fd(48 + (n % 10), fd);
	}
}
